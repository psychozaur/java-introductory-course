public class Rose extends Plant {

    @Override
    public void grow() {
        System.out.println("Rose grows");
    }

    @Override
    public void giveSmell() {
        System.out.println("Rose gives off a nice scent");
    }

    @Override
    public void wither() {
        System.out.println("Rose has to die");
    }
}
